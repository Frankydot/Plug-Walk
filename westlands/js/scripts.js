//user interface
$(document).ready(function(){
  $(".h2Sarabi").click(function(){
    $("#sarabi").show()
    $(".h2Sarabi").hide()
    $(".imgSarabi").hide()
  });
});

$(document).ready(function(){
  $("#backsarabi").click(function(){
  $("#sarabi").hide();
  $(".h2Sarabi").slideToggle()
  $(".imgSarabi").slideToggle()
});
});

